function somaValores(valor1: number, valor2: number): number {
    return valor1 + valor2;
};
console.log(somaValores(1, 2));

// Definindo o tipo de retorno da função é garantido que a função retornará o valor requerido, definindo apenas a entrada, garante apenas que a função aceite entradas de number ou qualquer outro tipo, porém não garante que o processo durante a função, não altere o seu retorno.
